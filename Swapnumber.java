package types;

public class Swapnumber {

	public static void main(String[] args) {
		
		
		int a,b,c;
		a=30;
		b=70;
		
		System.out.println("before swaping of a="+a+" and b="+b);
		
		c=a;
		a=b;
		b=c;
		
		System.out.println("after swaping of a="+a+" and b="+b);

		
		
		
	}

}
