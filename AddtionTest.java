package com.edu;

public class AddtionTest {

	
		int i,j,s;
		int add(int i,int j) {// Addition Process
			this.i=i;
			this.j=j;
			s=i+j;
			return s;
		}
		
		int sub(int i,int j) { //Subtraction Process
			this.i=i;
			this.j=j;
			s=i-j;
			return s;
		}
		int mul(int i,int j) { // Multiple Process
			this.i=i;
			this.j=j;
			s=i*j;
			return s;
		}
		int div(int i,int j) {  // Division Process
			this.i=i;
			this.j=j;
			s=i/j;
			return s;
		}
		
}